#include <stdlib.h>
#include <stdio.h>
#include <string.h>
#include <crypt.h>

/******************************************************************************
* Global definitions
*******************************************************************************/
#define MD5 "$1$"

/******************************************************************************
* Main code
*******************************************************************************/

int main(int argc, char const *argv[])
{
    const char string[] = "iloveyou";
    char * hash = crypt(string, MD5);

    printf("%s\n",hash);

    return 0;
}
